#include 	"proxy.h"

#include 	<iostream>

/* 
 * ===  FUNCTION  ======================================================================
 *         Name:  Proxy
 *  Description:  默认构造函数 
 * =====================================================================================
 */
Proxy::Proxy () : _mSubject(NULL) {
	std::cout << "Proxy Create" << std::endl;
}

/* 
 * ===  FUNCTION  ======================================================================
 *         Name:  ~Proxy
 *  Description:  默认析构函数 
 * =====================================================================================
 */
Proxy::~Proxy () {
}

/* 
 * ===  FUNCTION  ======================================================================
 *         Name:  Proxy  
 *  Description:  拷贝构造函数 
 * =====================================================================================
 */
Proxy::Proxy(const Proxy& theclass) {
}

/* 
 * ===  FUNCTION  ======================================================================
 *         Name:  Request 
 *  Description:  要求 
 * =====================================================================================
 */
void Proxy::Request() {
	if (_mSubject != NULL) {
		_mSubject->Request();
	}
}

/* 
 * ===  FUNCTION  ======================================================================
 *         Name:  Request 
 *  Description:  要求 
 * =====================================================================================
 */
void Proxy::InitCore(Subject* sub) {
	if ( _mSubject == NULL )
		_mSubject = sub; 
}
